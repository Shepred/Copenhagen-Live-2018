<h1>Privacy Policy</h1>

<p>The purpose of cphlive-vatsim.net is only to serve as a hub for information about the annual event. The controllers and pilots of the Copenhagen FIR & VATSIM Scandinavia do NOT have any commercial gains by running this website nor by hosting the event – it is purely for entertainment.</p>

<p>We believe in the importance of your security and privacy, hence cphlive-vatsim.net does NOT collect any other data than what you actively provide.</p>

<p>When signing up for a competition we collect your VATSIM ID, your email address as well as your preferences in terms of what competitions to enter.</p>

<p>We do so to be able to check if you’ve met the requirements to be eligible to win the competition and in the event that you win, contact you after the event so that you can collect your prize. Your email will NOT be kept for any other purpose nor given to any third party.</p>

<p>Once this purpose has been fulfilled we will delete the data provided by you.</p>

<p>If you would like to know what data we have collected about you, please reach out to the FIR Director of Copenhagen FIR on: denmark@vatsim-scandinavia.org.</p>

